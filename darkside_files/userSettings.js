$(document).ready(function() {
    /*----------------------------------------------------*/
    /*	Fancybox media
    /*----------------------------------------------------*/
    $(".fancybox").fancybox();
    /*----------------------------------------------------*/
    /*	Tooltips
    /*----------------------------------------------------*/
    $("a[data-rel=tooltip]").tooltip();
    /*----------------------------------------------------*/
    /*	Settings Panel
    /*----------------------------------------------------*/
    $('.switch').click(function(){

        if($('.settings').css('left') == '-220px'){
            $('.settings').animate({
                left: '-2px'
            });
        }
        else{
            $('.settings').animate({
                left: '-220px'
            });
        }
    });
});